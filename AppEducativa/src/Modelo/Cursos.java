
package Modelo;

public class Cursos {
    
    private int codigoCurso;
    private String nombreCurso;
    private String rfcProfesorCurso;

    public int getCodigoCurso() {
        return codigoCurso;
    }

    public void setCodigoCurso(int codigoCurso) {
        this.codigoCurso = codigoCurso;
    }

    public String getNombreCurso() {
        return nombreCurso;
    }

    public void setNombreCurso(String nombreCurso) {
        this.nombreCurso = nombreCurso;
    }

    public String getRfcProfesorCurso() {
        return rfcProfesorCurso;
    }

    public void setRfcProfesorCurso(String rfcProfesorCurso) {
        this.rfcProfesorCurso = rfcProfesorCurso;
    }
    
}
